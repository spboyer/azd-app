// buildInstructions creates integration instructions for the Bicep template.
func (g *BicepGenerator) buildInstructions(serviceTypes map[ResourceType]bool) BicepInstructions {
	steps := []string{
		"Save this template as infra/modules/diagnostic-settings.bicep in your project",
		"Ensure your main.bicep has a Log Analytics workspace resource or parameter",
		"Add module reference in main.bicep after your service resources",
		"Pass the required parameters (workspace ID and resource names)",
		"Run 'azd up' to deploy the diagnostic settings",
	}

	return BicepInstructions{
		Summary: "Add this module to your Bicep infrastructure to enable diagnostic settings",
		Steps:   steps,
	}
}

// buildParameters creates parameter documentation.
func (g *BicepGenerator) buildParameters() []BicepParameterInfo {
	return []BicepParameterInfo{
		{
			Name:        "logAnalyticsWorkspaceId",
			Description: "Resource ID of the Log Analytics Workspace where logs will be sent",
			Example:     "/subscriptions/00000000-0000-0000-0000-000000000000/resourceGroups/my-rg/providers/Microsoft.OperationalInsights/workspaces/my-workspace",
		},
	}
}
